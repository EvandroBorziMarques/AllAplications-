    private void calcular() {
        float real, dolar, valorr, valord, total, totall;
        
        DecimalFormat formatador = new DecimalFormat("0.00");
        
        
        real = Float.parseFloat(txtreal.getText().replace(",","."));
        valorr = Float.parseFloat(txtr.getText().replace(",","."));
        dolar = Float.parseFloat(txtdolar.getText().replace(",","."));
        valord = Float.parseFloat(txtd.getText().replace(",","."));
        //CÃ¡lculo:
        total = (dolar / real);
        totall = (valorr * valord);
        
        txttotalr.setText("" + formatador.format(total));
        txttotald.setText("" + formatador.format(totall));
    }

